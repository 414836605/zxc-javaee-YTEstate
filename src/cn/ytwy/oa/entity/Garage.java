package cn.ytwy.oa.entity;

public class Garage {
    private Integer garageId;

    private Integer garageArea;

    private Float garageFee;

    public Integer getGarageId() {
        return garageId;
    }

    public void setGarageId(Integer garageId) {
        this.garageId = garageId;
    }

    public Integer getGarageArea() {
        return garageArea;
    }

    public void setGarageArea(Integer garageArea) {
        this.garageArea = garageArea;
    }

    public Float getGarageFee() {
        return garageFee;
    }

    public void setGarageFee(Float garageFee) {
        this.garageFee = garageFee;
    }
}